def test_subprocess_velotype_help_includes_batch() -> None:
    r = subprocess.run(
        [sys.executable, "-m", "velotype", "--help"],
        cwd=_REPO_ROOT,
        capture_output=True,
        text=True,
        check=False,
        env=_env(),
    )
    assert r.returncode == 0
    out = r.stdout + (r.stderr or "")
    assert "batch" in out.lower()
